package ex_02_variable_and_Literals;

public class Lab003_operators {
    public static void main(String[] args) {
        /* There are numerous operators
        * Unary operator -- which works on single variable
        * assignment operator -- this operator basically is used for assignment
        * arithmetic operator -- this operator is used for some operations like addition , subtraction , division and multiplication
        * Relational operator -- establishes the relation between few variables and basis on that the operation is performed
        * logical operator --  this is logical OR and Logical AND */
        System.out.println(" test string ");
        boolean a = true;
        System.out.println(!a);
        System.out.println(!!a);
        boolean c = true || false; // logical operator
        System.out.println(c);
    }
}
